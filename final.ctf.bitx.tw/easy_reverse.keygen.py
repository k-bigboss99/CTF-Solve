#!/usr/bin/env python
first = [0x56, 0x59, 0x4f, 0x7a, 0x32, 0x60, 0x34, 0x78, 0x5e, 0x73, 0x32]
second = [0x0d, 0x48, 0x09, 0x08, 0x48, 0x34, 0x78, 0x7f, 0x4b, 0x03, 0x12]

for i in range(0, 11):
    for c in range(0,256):
        if c ^ 1 == first[i]:
            print(chr(c), end='')

for i in range(0, 11):
    for c in range(0,256):
        if ((c ^ 0x20) - 10) ^ 0x41 == second[i]:
            print(chr(c), end='')

print()
